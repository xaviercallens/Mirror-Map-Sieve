def run_discovery_pipeline(max_n: int = 25, output_dir: str = "output/discovery"):
    """Execute the full discovery pipeline.

    1. Generate candidates
    2. Falsify computationally
    3. Fit closed forms for unknowns
    4. Check non-triviality
    5. Check novelty
    6. Generate Lean 4 code
    7. Report results
    """
    t0 = time.time()
    out_dir = Path(output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    print("=" * 72)
    print("  🔬 SOCRATE AI LAB — MATHEMATICAL DISCOVERY PIPELINE")
    print("  Combinatorial Identity Discovery Engine v0.1.0")
    print("=" * 72)

    # ── Step 1: Generate ───────────────────────────────────────────
    print("\n📋 Step 1: Generating candidates...")
    candidates = generate_all_candidates()
    print(f"   Generated {len(candidates)} candidates across "
          f"{len(set(c.family for c in candidates))} families")

    # ── Step 2: Falsify ────────────────────────────────────────────
    print(f"\n🧪 Step 2: Computational falsification (n ≤ {max_n})...")
    pipeline_results = []

    for i, candidate in enumerate(candidates, 1):
        result = {"candidate": candidate.to_dict()}

        # Test
        test = test_identity(candidate, max_n=max_n)
        result["test"] = {
            "status": test["status"],
            "num_tests": len(test["tests"]),
        }

        # Fit (for discovery targets)
        if test["status"] in ("NEEDS_FITTING", "FALSIFIED") and \
           candidate.source == "discovery target":
            fit = discover_closed_form(candidate, max_n=max_n + 10)
            result["fit"] = {
                "status": fit.get("status"),
                "polynomial_degree": fit.get("polynomial_degree"),
                "closed_form_hint": fit.get("closed_form_hint"),
                "fit_verified": fit.get("fit_verified"),
            }

            # Print first few values for manual inspection
            vals = []
            for n in range(candidate.min_n, min(candidate.min_n + 8, max_n)):
                vals.append(f"f({n})={candidate.lhs_func(n)}")
            result["sample_values"] = vals

        # Non-triviality
        triv = check_non_triviality(candidate)
        result["triviality"] = triv

        # Novelty
        nov = check_novelty(candidate)
        result["novelty"] = nov

        # Status symbol
        status_icon = {
            "VERIFIED": "✅", "FALSIFIED": "❌",
            "NEEDS_FITTING": "🔍", "ERROR": "⚠️"
        }.get(test["status"], "?")
        trivial_icon = "🔵" if triv["is_trivial"] else "🟢"
        known_icon = "📚" if nov["is_known"] else "🌟"

        print(f"   [{i:2d}] {status_icon}{trivial_icon}{known_icon} "
              f"{candidate.name} "
              f"(complexity={triv['complexity_score']}, "
              f"novelty={nov['novelty_score']})")

        if result.get("fit", {}).get("closed_form_hint"):
            print(f"        → {result['fit']['closed_form_hint']}")

        pipeline_results.append(result)

    # ── Step 3: Summary ────────────────────────────────────────────
    elapsed = time.time() - t0

    verified = [r for r in pipeline_results
                if r["test"]["status"] == "VERIFIED"]
    falsified = [r for r in pipeline_results
                 if r["test"]["status"] == "FALSIFIED"]
    fitted = [r for r in pipeline_results
              if r.get("fit", {}).get("status") == "FITTED"]
    non_trivial = [r for r in pipeline_results
                   if not r["triviality"]["is_trivial"]]
    potentially_new = [r for r in pipeline_results
                       if r["novelty"]["novelty_score"] >= 3]

    print(f"\n{'=' * 72}")
    print(f"  📊 DISCOVERY PIPELINE RESULTS")
    print(f"{'=' * 72}")
    print(f"  Total candidates:      {len(candidates)}")
    print(f"  ✅ Verified:           {len(verified)}")
    print(f"  ❌ Falsified:          {len(falsified)}")
    print(f"  🔍 Fitted (closed form): {len(fitted)}")
    print(f"  🟢 Non-trivial:        {len(non_trivial)}")
    print(f"  🌟 Potentially new:    {len(potentially_new)}")
    print(f"  ⏱️  Elapsed:            {elapsed:.2f}s")
    print(f"{'=' * 72}")

    # Report fitted discoveries
    if fitted:
        print(f"\n  🎯 CLOSED FORMS DISCOVERED:")
        for r in fitted:
            name = r["candidate"]["name"]
            hint = r["fit"].get("closed_form_hint", "?")
            degree = r["fit"].get("polynomial_degree", "?")
            print(f"     {name}: {hint} (degree {degree})")

    # Report potentially new identities
    if potentially_new:
        print(f"\n  🌟 POTENTIALLY NEW IDENTITIES:")
        for r in potentially_new:
            name = r["candidate"]["name"]
            lhs = r["candidate"]["lhs_expr"]
            print(f"     {name}: {lhs}")
            if r.get("fit", {}).get("closed_form_hint"):
                print(f"       → {r['fit']['closed_form_hint']}")

    # ── Step 4: Save results ───────────────────────────────────────
    report = {
        "pipeline": "combinatorial_identity_discovery",
        "version": "0.1.0",
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ"),
        "config": {"max_n": max_n},
        "summary": {
            "total": len(candidates),
            "verified": len(verified),
            "falsified": len(falsified),
            "fitted": len(fitted),
            "non_trivial": len(non_trivial),
            "potentially_new": len(potentially_new),
            "elapsed_s": round(elapsed, 2),
        },
        "results": pipeline_results,
    }

    report_path = out_dir / "discovery_report.json"
    with open(report_path, "w") as f:
        json.dump(report, f, indent=2, default=str)
    print(f"\n  Report saved to: {report_path}")

    return report


def main():
    parser = argparse.ArgumentParser(
        description="Combinatorial Identity Discovery Pipeline"
    )
    parser.add_argument("--max-n", type=int, default=25,
                        help="Maximum n for testing (default: 25)")
    parser.add_argument("--output-dir", type=str,
                        default="output/discovery",
                        help="Output directory (default: output/discovery)")
    args = parser.parse_args()

    run_discovery_pipeline(max_n=args.max_n, output_dir=args.output_dir)


if __name__ == "__main__":
    main()
