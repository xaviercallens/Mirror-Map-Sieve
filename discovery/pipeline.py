# NON-TRIVIALITY CHECKER
# ============================================================================
# LESSON FROM ALIEN MATH: Many "verified" theorems were definitional
# tautologies. This checker ensures discovered identities are non-trivial.

def check_non_triviality(candidate: CandidateIdentity) -> dict:
    """Check if an identity is non-trivial.

    An identity is trivial if:
    1. LHS and RHS are syntactically identical (tautology)
    2. Both sides are constant (independent of n)
    3. The identity follows from a single application of a known rule
    4. The RHS is 0 and the LHS is an alternating sum (trivial cancellation)

    Returns a dict with:
    - is_trivial: bool
    - reason: why it's trivial (if applicable)
    - complexity_score: estimate of mathematical depth
    """
    result = {"is_trivial": False, "reason": None, "complexity_score": 0}

    # Check 1: Is the identity self-referential?
    if candidate.lhs_expr == candidate.rhs_expr:
        result["is_trivial"] = True
        result["reason"] = "LHS = RHS (tautology)"
        return result

    # Check 2: Are the values constant?
    values = set()
    for n in range(max(candidate.min_n, 1), min(candidate.min_n + 10, 20)):
        try:
            values.add(candidate.lhs_func(n))
        except Exception:
            pass
    if len(values) <= 1:
        result["is_trivial"] = True
        result["reason"] = f"LHS is constant (= {values.pop() if values else '?'})"
        return result

    # Check 3: Complexity scoring
    # Higher score = more interesting
    score = 0

    # Points for involving sums (not just evaluation)
    if "Σ" in candidate.lhs_expr or "sum" in candidate.lhs_expr.lower():
        score += 2

    # Points for higher-power weights
    for p in range(2, 7):
        if f"k{p}" in candidate.lhs_expr or f"k^{p}" in candidate.lhs_expr or f"k²" in candidate.lhs_expr:
            score += p

    # Points for combining different families
    if "C(n,k)²" in candidate.lhs_expr or "C(n,k)^2" in candidate.lhs_expr:
        score += 3

    # Points for alternating signs
    if "(-1)" in candidate.lhs_expr:
        score += 2

    # Points for non-obvious closed form
    if "2^" in candidate.rhs_expr and "·" in candidate.rhs_expr:
        score += 2

    # Points for discovery targets
    if candidate.source == "discovery target":
        score += 5

    result["complexity_score"] = score
    return result


